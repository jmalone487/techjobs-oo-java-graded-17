package org.launchcode.techjobs.oo;

import java.util.Objects;

public class PositionType {

    private int id;
    private static int nextId = 1;
    private String value;

    public PositionType() {
        id = nextId;
        nextId++;
    }

    public PositionType(String value) {
        this();  // Calls the default constructor to initialize id
        this.value = value;
    }

    // Custom toString method that returns the value of PositionType
    @Override
    public String toString() {
        return value != null ? value : "Data not available";
    }

    // Custom equals method: considers two PositionType objects equal if their id fields are the same
    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof PositionType)) return false;
        PositionType that = (PositionType) o;
        return getId() == that.getId();
    }

    // Custom hashCode method: uses id to generate hashCode
    @Override
    public int hashCode() {
        return Objects.hash(getId());
    }

    // Getters and Setters:

    public int getId() {
        return id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
